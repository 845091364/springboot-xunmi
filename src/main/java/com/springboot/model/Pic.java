package com.springboot.model;

public class Pic {
	private String id;
	private String pic;
	private String oldPic;
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getPic() {
		return pic;
	}
	public void setPic(String pic) {
		this.pic = pic;
	}
	public String getOldPic() {
		return oldPic;
	}
	public void setOldPic(String oldPic) {
		this.oldPic = oldPic;
	}
}
